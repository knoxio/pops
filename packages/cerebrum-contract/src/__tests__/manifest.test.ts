import { execFileSync } from 'node:child_process';
import { readFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { describe, expect, expectTypeOf, it } from 'vitest';

import type { CerebrumContract } from '../manifest.js';
import type { Engram } from '../types/engram.js';
import type { Nudge } from '../types/nudge.js';
import type { Scope } from '../types/scope.js';

const HERE = dirname(fileURLToPath(import.meta.url));
const PACKAGE_ROOT = resolve(HERE, '..', '..');
const GENERATED_PATH = resolve(PACKAGE_ROOT, 'src', 'manifest.generated.ts');
const AUTOGENERATED_HEADER = '// AUTOGENERATED by scripts/generate-manifest.ts. Do not edit.';

describe('CerebrumContract manifest type generation', () => {
  it('exposes Engram on entities.engram with byte-equal shape', () => {
    expectTypeOf<CerebrumContract['entities']['engram']>().toEqualTypeOf<Engram>();
  });

  it('exposes Nudge on entities.nudge with byte-equal shape', () => {
    expectTypeOf<CerebrumContract['entities']['nudge']>().toEqualTypeOf<Nudge>();
  });

  it('exposes Scope on entities.scope with byte-equal shape', () => {
    expectTypeOf<CerebrumContract['entities']['scope']>().toEqualTypeOf<Scope>();
  });

  it('pins pillar discriminant and exposes errors/router slots', () => {
    expectTypeOf<CerebrumContract['pillar']>().toEqualTypeOf<'cerebrum'>();
    expectTypeOf<CerebrumContract['version']>().toEqualTypeOf<string>();
    expectTypeOf<CerebrumContract>().toHaveProperty('errors');
    expectTypeOf<CerebrumContract>().toHaveProperty('router');
  });

  it('emits the AUTOGENERATED header so hand-edits fail loudly', () => {
    const contents = readFileSync(GENERATED_PATH, 'utf8');
    expect(contents).toContain(AUTOGENERATED_HEADER);
  });

  it('regeneration is idempotent (no diff on a second run)', () => {
    const before = readFileSync(GENERATED_PATH, 'utf8');
    execFileSync('pnpm', ['--silent', 'run', 'generate:manifest'], {
      cwd: PACKAGE_ROOT,
      stdio: ['ignore', 'ignore', 'inherit'],
    });
    const after = readFileSync(GENERATED_PATH, 'utf8');
    expect(after).toBe(before);
  });
});
