import { execFileSync } from 'node:child_process';
import { readFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { describe, expect, expectTypeOf, it } from 'vitest';

import type { FinanceContract } from '../manifest.js';
import type { Budget } from '../types/budget.js';
import type { Entity } from '../types/entity.js';
import type { Transaction } from '../types/transaction.js';
import type { WishListItem } from '../types/wish-list-item.js';

const HERE = dirname(fileURLToPath(import.meta.url));
const PACKAGE_ROOT = resolve(HERE, '..', '..');
const GENERATED_PATH = resolve(PACKAGE_ROOT, 'src', 'manifest.generated.ts');
const AUTOGENERATED_HEADER = '// AUTOGENERATED by scripts/generate-manifest.ts. Do not edit.';

describe('FinanceContract manifest type generation', () => {
  it('exposes WishListItem on entities.wishListItem with byte-equal shape', () => {
    expectTypeOf<FinanceContract['entities']['wishListItem']>().toEqualTypeOf<WishListItem>();
  });

  it('exposes Transaction on entities.transaction with byte-equal shape', () => {
    expectTypeOf<FinanceContract['entities']['transaction']>().toEqualTypeOf<Transaction>();
  });

  it('exposes Budget on entities.budget with byte-equal shape', () => {
    expectTypeOf<FinanceContract['entities']['budget']>().toEqualTypeOf<Budget>();
  });

  it('exposes Entity on entities.entity with byte-equal shape', () => {
    expectTypeOf<FinanceContract['entities']['entity']>().toEqualTypeOf<Entity>();
  });

  it('pins pillar discriminant and exposes errors/router slots', () => {
    expectTypeOf<FinanceContract['pillar']>().toEqualTypeOf<'finance'>();
    expectTypeOf<FinanceContract['version']>().toEqualTypeOf<string>();
    expectTypeOf<FinanceContract>().toHaveProperty('errors');
    expectTypeOf<FinanceContract>().toHaveProperty('router');
  });

  it('emits the AUTOGENERATED header so hand-edits fail loudly', () => {
    const contents = readFileSync(GENERATED_PATH, 'utf8');
    expect(contents).toContain(AUTOGENERATED_HEADER);
  });

  it('regeneration is idempotent (no diff on a second run)', () => {
    const before = readFileSync(GENERATED_PATH, 'utf8');
    execFileSync('pnpm', ['--silent', 'run', 'generate:manifest'], {
      cwd: PACKAGE_ROOT,
      stdio: ['ignore', 'ignore', 'inherit'],
    });
    const after = readFileSync(GENERATED_PATH, 'utf8');
    expect(after).toBe(before);
  });
});
