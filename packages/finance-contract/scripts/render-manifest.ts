import { readFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

export const MANIFEST_HEADER = '// AUTOGENERATED by scripts/generate-manifest.ts. Do not edit.';

const HERE = dirname(fileURLToPath(import.meta.url));
const PACKAGE_JSON_PATH = resolve(HERE, '..', 'package.json');

export function readContractVersion(): string {
  const pkg: unknown = JSON.parse(readFileSync(PACKAGE_JSON_PATH, 'utf8'));
  if (
    typeof pkg !== 'object' ||
    pkg === null ||
    !('version' in pkg) ||
    typeof (pkg as { version: unknown }).version !== 'string'
  ) {
    throw new Error('package.json must declare a string `version` field');
  }
  return (pkg as { version: string }).version;
}

export function renderManifest(version: string): string {
  return `${MANIFEST_HEADER}
// Regenerated by \`pnpm -F @pops/finance-contract generate:manifest\`.
// Source version pinned at generation time: ${version}
import type { Budget } from './types/budget.js';
import type { Entity } from './types/entity.js';
import type { Transaction } from './types/transaction.js';
import type { WishListItem } from './types/wish-list-item.js';
import type { FinanceError } from './errors.js';
import type { FinanceRouter } from './router.js';

export interface FinanceContract {
  readonly pillar: 'finance';
  readonly version: string;
  readonly entities: {
    readonly budget: Budget;
    readonly entity: Entity;
    readonly transaction: Transaction;
    readonly wishListItem: WishListItem;
  };
  readonly errors: FinanceError;
  readonly router: FinanceRouter;
}
`;
}

export const MANIFEST_OUTPUT_PATH = resolve(HERE, '..', 'src', 'manifest.generated.ts');
