import { readFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

export const MANIFEST_HEADER = '// AUTOGENERATED by scripts/generate-manifest.ts. Do not edit.';

const HERE = dirname(fileURLToPath(import.meta.url));
const PACKAGE_JSON_PATH = resolve(HERE, '..', 'package.json');

export function readContractVersion(): string {
  const pkg: unknown = JSON.parse(readFileSync(PACKAGE_JSON_PATH, 'utf8'));
  if (
    typeof pkg !== 'object' ||
    pkg === null ||
    !('version' in pkg) ||
    typeof (pkg as { version: unknown }).version !== 'string'
  ) {
    throw new Error('package.json must declare a string `version` field');
  }
  return (pkg as { version: string }).version;
}

export function renderManifest(version: string): string {
  return `${MANIFEST_HEADER}
// Regenerated by \`pnpm -F @pops/food-contract generate:manifest\`.
// Source version pinned at generation time: ${version}
import type { Ingredient } from './types/ingredient.js';
import type { MealPlan } from './types/meal-plan.js';
import type { Recipe } from './types/recipe.js';
import type { FoodError } from './errors.js';
import type { FoodRouter } from './router.js';

export interface FoodContract {
  readonly pillar: 'food';
  readonly version: string;
  readonly entities: {
    readonly ingredient: Ingredient;
    readonly mealPlan: MealPlan;
    readonly recipe: Recipe;
  };
  readonly errors: FoodError;
  readonly router: FoodRouter;
}
`;
}

export const MANIFEST_OUTPUT_PATH = resolve(HERE, '..', 'src', 'manifest.generated.ts');
