import { execFileSync } from 'node:child_process';
import { readFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { describe, expect, expectTypeOf, it } from 'vitest';

import type { FoodContract } from '../manifest.js';
import type { Ingredient } from '../types/ingredient.js';
import type { MealPlan } from '../types/meal-plan.js';
import type { Recipe } from '../types/recipe.js';

const HERE = dirname(fileURLToPath(import.meta.url));
const PACKAGE_ROOT = resolve(HERE, '..', '..');
const GENERATED_PATH = resolve(PACKAGE_ROOT, 'src', 'manifest.generated.ts');
const AUTOGENERATED_HEADER = '// AUTOGENERATED by scripts/generate-manifest.ts. Do not edit.';

describe('FoodContract manifest type generation', () => {
  it('exposes Recipe on entities.recipe with byte-equal shape', () => {
    expectTypeOf<FoodContract['entities']['recipe']>().toEqualTypeOf<Recipe>();
  });

  it('exposes MealPlan on entities.mealPlan with byte-equal shape', () => {
    expectTypeOf<FoodContract['entities']['mealPlan']>().toEqualTypeOf<MealPlan>();
  });

  it('exposes Ingredient on entities.ingredient with byte-equal shape', () => {
    expectTypeOf<FoodContract['entities']['ingredient']>().toEqualTypeOf<Ingredient>();
  });

  it('pins pillar discriminant and exposes errors/router slots', () => {
    expectTypeOf<FoodContract['pillar']>().toEqualTypeOf<'food'>();
    expectTypeOf<FoodContract['version']>().toEqualTypeOf<string>();
    expectTypeOf<FoodContract>().toHaveProperty('errors');
    expectTypeOf<FoodContract>().toHaveProperty('router');
  });

  it('emits the AUTOGENERATED header so hand-edits fail loudly', () => {
    const contents = readFileSync(GENERATED_PATH, 'utf8');
    expect(contents).toContain(AUTOGENERATED_HEADER);
  });

  it('regeneration is idempotent (no diff on a second run)', () => {
    const before = readFileSync(GENERATED_PATH, 'utf8');
    execFileSync('pnpm', ['--silent', 'run', 'generate:manifest'], {
      cwd: PACKAGE_ROOT,
      stdio: ['ignore', 'ignore', 'inherit'],
    });
    const after = readFileSync(GENERATED_PATH, 'utf8');
    expect(after).toBe(before);
  });
});
