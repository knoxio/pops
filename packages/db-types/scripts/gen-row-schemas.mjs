#!/usr/bin/env node
/**
 * Generate drizzle-zod row schemas for every Drizzle table in
 * `packages/db-types/src/schema`.
 *
 * For each `*.ts` schema file (excluding `index.ts`, `row-schemas.ts`,
 * and any pre-generated `*-row-schemas.ts`), emit a sibling
 * `<basename>-row-schemas.ts` exporting `<table>RowSchema =
 * createSelectSchema(<table>)` for every `sqliteTable(...)` binding
 * the source file declares. The schema dir also gets a
 * `row-schemas.ts` index that re-exports every sibling.
 *
 * Run with `--check` to fail (exit 1) when a sibling is missing or
 * its export set drifts from the source schema. The check is
 * semantic, not byte-for-byte: it tolerates formatter rewrites
 * (import wrapping, trailing newlines) — those are caught by
 * `pnpm format:check` instead.
 *
 * Workflow after editing a schema file:
 *   1. `pnpm --filter @pops/db-types gen:row-schemas`
 *   2. `pnpm format`
 *   3. commit
 */
import { readFileSync, readdirSync, statSync, writeFileSync } from 'node:fs';
import { dirname, join, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const schemaRoot = resolve(here, '..', 'src', 'schema');
const repoRoot = resolve(here, '..', '..', '..');

const checkOnly = process.argv.includes('--check');

const HEADER = `// AUTOGENERATED by scripts/gen-row-schemas.mjs — do not edit by hand.
// Run \`pnpm --filter @pops/db-types gen:row-schemas\` to regenerate.
`;

/** @returns {string[]} */
function walk(dir) {
  /** @type {string[]} */
  const out = [];
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    const stat = statSync(full);
    if (stat.isDirectory()) {
      out.push(...walk(full));
    } else if (
      entry.endsWith('.ts') &&
      entry !== 'index.ts' &&
      entry !== 'row-schemas.ts' &&
      !entry.endsWith('-row-schemas.ts')
    ) {
      out.push(full);
    }
  }
  return out;
}

/**
 * Extract the names of every `export const X = sqliteTable(...)`
 * binding declared in `source`. The regex matches the declaration
 * head so both the `sqliteTable('name', { ... })` and the
 * `sqliteTable('name', { ... }, (table) => [...])` forms parse.
 *
 * @param {string} source
 * @returns {string[]}
 */
function extractTables(source) {
  const pattern = /export\s+const\s+([A-Za-z_$][\w$]*)\s*=\s*sqliteTable\s*\(/g;
  /** @type {string[]} */
  const names = [];
  for (const match of source.matchAll(pattern)) {
    names.push(match[1]);
  }
  return names;
}

/**
 * @param {string} basename - schema file basename without `.ts`
 * @param {string[]} tables
 * @returns {string}
 */
function renderSibling(basename, tables) {
  const importList = tables.join(', ');
  const exports = tables
    .map((t) => `export const ${t}RowSchema = createSelectSchema(${t});`)
    .join('\n');
  return `${HEADER}\nimport { createSelectSchema } from 'drizzle-zod';\n\nimport { ${importList} } from './${basename}.js';\n\n${exports}\n`;
}

/**
 * @param {Array<{ relPath: string }>} entries
 * @returns {string}
 */
function renderIndex(entries) {
  const lines = entries
    .slice()
    .toSorted((a, b) => a.relPath.localeCompare(b.relPath))
    .map((entry) => `export * from './${entry.relPath.replace(/\.ts$/, '.js')}';`);
  return `${HEADER}\n${lines.join('\n')}\n`;
}

/**
 * Semantic check that `siblingSource` exports a
 * `<table>RowSchema = createSelectSchema(<table>)` binding for
 * every table in `expected`. Tolerates formatter-introduced
 * whitespace; reports the first missing export.
 *
 * @param {string} siblingSource
 * @param {string[]} expected
 * @returns {string | null} - missing table name, or null if all present
 */
function findMissingExport(siblingSource, expected) {
  for (const table of expected) {
    const pattern = new RegExp(
      `export\\s+const\\s+${table}RowSchema\\s*=\\s*createSelectSchema\\(\\s*${table}\\s*\\)`
    );
    if (!pattern.test(siblingSource)) return table;
  }
  return null;
}

const sources = walk(schemaRoot).toSorted();
/** @type {Array<{ siblingPath: string; relPath: string; basename: string; tables: string[] }>} */
const generated = [];

for (const file of sources) {
  const source = readFileSync(file, 'utf8');
  const tables = extractTables(source);
  if (tables.length === 0) continue;
  const basename = file.replace(/\.ts$/, '').split('/').pop();
  const siblingPath = file.replace(/\.ts$/, '-row-schemas.ts');
  generated.push({
    siblingPath,
    relPath: relative(schemaRoot, siblingPath),
    basename,
    tables,
  });
}

const indexPath = join(schemaRoot, 'row-schemas.ts');

/** @type {Array<{ path: string; reason: string }>} */
const problems = [];

if (checkOnly) {
  for (const entry of generated) {
    checkSibling(entry, problems);
  }
  checkBarrel(indexPath, generated, problems);
} else {
  for (const entry of generated) {
    writeFileSync(entry.siblingPath, renderSibling(entry.basename, entry.tables));
  }
  writeFileSync(indexPath, renderIndex(generated));
}

if (checkOnly && problems.length > 0) {
  process.stderr.write(
    `row-schemas check failed (${problems.length} issue${problems.length === 1 ? '' : 's'}):\n${problems
      .map((p) => `  - ${p.path}: ${p.reason}`)
      .join(
        '\n'
      )}\nRun \`pnpm --filter @pops/db-types gen:row-schemas && pnpm format\` to regenerate.\n`
  );
  process.exit(1);
}

process.stdout.write(
  `${checkOnly ? 'Checked' : 'Wrote'} ${generated.length} sibling row-schema files (${generated.reduce(
    (acc, e) => acc + e.tables.length,
    0
  )} tables).\n`
);

/**
 * @param {{ siblingPath: string; tables: string[] }} entry
 * @param {Array<{ path: string; reason: string }>} problems
 */
function checkSibling(entry, problems) {
  const existing = safeRead(entry.siblingPath);
  const path = relative(repoRoot, entry.siblingPath);
  if (existing === null) {
    problems.push({ path, reason: 'sibling row-schemas file is missing' });
    return;
  }
  const missing = findMissingExport(existing, entry.tables);
  if (missing !== null) {
    problems.push({
      path,
      reason: `missing export \`${missing}RowSchema = createSelectSchema(${missing})\``,
    });
  }
}

/**
 * @param {string} barrelPath
 * @param {Array<{ relPath: string }>} entries
 * @param {Array<{ path: string; reason: string }>} problems
 */
function checkBarrel(barrelPath, entries, problems) {
  const path = relative(repoRoot, barrelPath);
  const indexSource = safeRead(barrelPath);
  if (indexSource === null) {
    problems.push({ path, reason: 'row-schemas barrel file is missing' });
    return;
  }
  for (const entry of entries) {
    const expected = `./${entry.relPath.replace(/\.ts$/, '.js')}`;
    if (!indexSource.includes(expected)) {
      problems.push({ path, reason: `barrel is missing re-export of \`${expected}\`` });
    }
  }
}

/** @param {string} path */
function safeRead(path) {
  try {
    return readFileSync(path, 'utf8');
  } catch {
    return null;
  }
}
