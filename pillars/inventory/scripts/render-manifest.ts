import { readFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

export const MANIFEST_HEADER = '// AUTOGENERATED by scripts/generate-manifest.ts. Do not edit.';

const HERE = dirname(fileURLToPath(import.meta.url));
const PACKAGE_JSON_PATH = resolve(HERE, '..', 'package.json');

export function readContractVersion(): string {
  const pkg: unknown = JSON.parse(readFileSync(PACKAGE_JSON_PATH, 'utf8'));
  if (
    typeof pkg !== 'object' ||
    pkg === null ||
    !('version' in pkg) ||
    typeof (pkg as { version: unknown }).version !== 'string'
  ) {
    throw new Error('package.json must declare a string `version` field');
  }
  return (pkg as { version: string }).version;
}

export function renderManifest(version: string): string {
  return `${MANIFEST_HEADER}
// Regenerated by \`pnpm -F @pops/inventory generate:manifest\`.
// Source version pinned at generation time: ${version}
import type { Connection } from './types/connection.js';
import type { Item } from './types/item.js';
import type { Location } from './types/location.js';
import type { Warranty } from './types/warranty.js';
import type { InventoryError } from './errors.js';
import type { InventoryRouter } from './router.js';

export interface InventoryContract {
  readonly pillar: 'inventory';
  readonly version: string;
  readonly entities: {
    readonly connection: Connection;
    readonly item: Item;
    readonly location: Location;
    readonly warranty: Warranty;
  };
  readonly errors: InventoryError;
  readonly router: InventoryRouter;
}
`;
}

export const MANIFEST_OUTPUT_PATH = resolve(HERE, '..', 'src', 'contract', 'manifest.generated.ts');
