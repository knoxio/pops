import { readFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

export const MANIFEST_HEADER = '// AUTOGENERATED by scripts/generate-manifest.ts. Do not edit.';

const HERE = dirname(fileURLToPath(import.meta.url));
const PACKAGE_JSON_PATH = resolve(HERE, '..', 'package.json');

export function readContractVersion(): string {
  const pkg: unknown = JSON.parse(readFileSync(PACKAGE_JSON_PATH, 'utf8'));
  if (
    typeof pkg !== 'object' ||
    pkg === null ||
    !('version' in pkg) ||
    typeof (pkg as { version: unknown }).version !== 'string'
  ) {
    throw new Error('package.json must declare a string `version` field');
  }
  return (pkg as { version: string }).version;
}

export function renderManifest(version: string): string {
  return `${MANIFEST_HEADER}
// Regenerated by \`pnpm -F @pops/cerebrum generate:manifest\`.
// Source version pinned at generation time: ${version}
import type { Engram } from './types/engram.js';
import type { Nudge } from './types/nudge.js';
import type { Scope } from './types/scope.js';
import type { CerebrumError } from './errors.js';
import type { CerebrumRouter } from './router.js';

export interface CerebrumContract {
  readonly pillar: 'cerebrum';
  readonly version: string;
  readonly entities: {
    readonly engram: Engram;
    readonly nudge: Nudge;
    readonly scope: Scope;
  };
  readonly errors: CerebrumError;
  readonly router: CerebrumRouter;
}
`;
}

export const MANIFEST_OUTPUT_PATH = resolve(HERE, '..', 'src', 'contract', 'manifest.generated.ts');
